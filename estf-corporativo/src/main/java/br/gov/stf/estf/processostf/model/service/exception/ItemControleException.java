package br.gov.stf.estf.processostf.model.service.exception;

public class ItemControleException extends Exception{

	private static final long serialVersionUID = -6980800701083311555L;
	
	public ItemControleException() {
	}
	
	public ItemControleException(String message) {
		super(message);
	}

	public ItemControleException(Throwable cause) {
		super(cause);
	}

	public ItemControleException(String message, Throwable cause) {
		super(message, cause);
	}
	
	

}
