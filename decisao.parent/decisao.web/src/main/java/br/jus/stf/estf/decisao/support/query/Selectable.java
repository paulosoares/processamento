package br.jus.stf.estf.decisao.support.query;

/**
 * Interface que deve ser implementada por objetos "selecionáveis", por 
 * exemplo em uma coleção de resultados.
 * 
 * @author Rodrigo Barreiros
 * @since 07.04.2010
 */
public interface Selectable {
    
	/**
	 * Indica se o objeto está, ou não, selecionado. Retorna um valor boleano.
	 * 
	 * @return true, se selecionado, false, caso contrário.
	 */
    boolean isSelected();
    
    /**
     * Seta a flag de seleção do objeto corrente.
     * 
     * @param selected o indicador de seleção
     */
    void setSelected(boolean selected);

}
